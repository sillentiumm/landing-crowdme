document.querySelector('.features-number_1').addEventListener('mouseover', () => {
    document.querySelector('.features-number_1').classList.toggle('psevdo');
    document.querySelector('.features-number__img_1').style.backgroundColor = "#add136";
});

document.querySelector('.features-number_1').addEventListener('mouseout', () => {
    document.querySelector('.features-number_1').classList.toggle('psevdo');
    document.querySelector('.features-number__img_1').style.backgroundColor = "#5f5f5f";
});

